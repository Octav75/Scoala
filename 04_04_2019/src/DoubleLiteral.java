public class DoubleLiteral {
    public static void main(String[] args) {

        double d = 3.23523;
        double d1 = d * 100;
        System.out.println(d1);
        int d2 = (int) d1;
        System.out.println(d2);
        int d3 = d2 / 100;
        System.out.println(d3);
        double d4 = (double) d2 / 100;
        System.out.println(d4);
        }
}
